package opsknowledge

import (
	"fmt"

	corpus "github.com/globulario/services/golang/opsknowledge"
	"gopkg.in/yaml.v3"
)

// FileRecall is the generated flat-recall artifact, distinct from the behavioral
// catalogs. ai-memory embeds it and self-seeds it into the `memories` table at
// startup, so operational-knowledge recall is present without a day-0/day-1 CLI
// seed (which needs auth that may not be ready at bootstrap, and which silently
// defers with no automatic retry). Self-seeding writes the local store directly,
// so it is auth-free and self-healing across restarts.
const FileRecall = "recall.generated.yaml"

// RecallEntry is one flat operational-knowledge recall record. It mirrors the
// fields the `ops-knowledge seed` CLI writes per memory, plus the canonical
// per-entry seed hash used for idempotent upsert.
type RecallEntry struct {
	ID         string   `yaml:"id"`
	Type       string   `yaml:"type"`
	Title      string   `yaml:"title"`
	Tags       []string `yaml:"tags,omitempty"`
	Content    string   `yaml:"content"`
	RelatedIDs []string `yaml:"related_ids,omitempty"`
	SeedSHA256 string   `yaml:"seed_sha256"`
}

// RenderRecall compiles the operational-knowledge corpus into the flat recall
// artifact bytes (path-stable order, canonical hash per entry).
func RenderRecall(corpusDir string) ([]byte, error) {
	files, err := corpus.LoadDir(corpusDir)
	if err != nil {
		return nil, err
	}
	var entries []RecallEntry
	for _, f := range files {
		for _, e := range f.Entries {
			h, err := corpus.HashEntry(e)
			if err != nil {
				return nil, fmt.Errorf("hash %s: %w", e.ID, err)
			}
			entries = append(entries, RecallEntry{
				ID:         e.ID,
				Type:       e.Type,
				Title:      e.Title,
				Tags:       e.Tags,
				Content:    e.Content,
				RelatedIDs: e.RelatedIDs,
				SeedSHA256: h,
			})
		}
	}
	body, err := yaml.Marshal(entries)
	if err != nil {
		return nil, err
	}
	header := "# GENERATED by opscompile (RenderRecall) — DO NOT EDIT.\n" +
		"# Flat operational-knowledge recall; ai-memory self-seeds this at startup.\n"
	return append([]byte(header), body...), nil
}
