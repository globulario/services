package opsknowledge

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"

	"github.com/globulario/services/golang/ai_memory/behavioral/domain"
	"gopkg.in/yaml.v3"
)

// generatedHeader marks every emitted file as a build artifact.
const generatedHeader = "# Code generated by operational knowledge compiler; DO NOT EDIT.\n" +
	"# source corpus: docs/operational-knowledge/\n"

// File names for the generated bundle.
const (
	FileAuthorities      = "authorities.generated.yaml"
	FileConditions       = "conditions.generated.yaml"
	FileForbiddenMoves   = "forbidden_moves.generated.yaml"
	FileRequiredEvidence = "required_evidence.generated.yaml"
	FilePrinciples       = "principles.generated.yaml"
	FileSources          = "sources.generated.yaml"
)

// emitPrinciple mirrors the cluster_operator pack's principle YAML shape so
// generated principles parse identically to hand-authored ones.
type emitPrinciple struct {
	ID                string   `yaml:"id"`
	Title             string   `yaml:"title"`
	AppliesWhen       []string `yaml:"applies_when"`
	Authorities       []string `yaml:"authorities,omitempty"`
	RequiredEvidence  []string `yaml:"required_evidence"`
	ForbiddenMoves    []string `yaml:"forbidden_moves"`
	RecommendedAction string   `yaml:"recommended_action"`
	RiskLevel         string   `yaml:"risk_level"`
	RevocationRule    string   `yaml:"revocation_rule"`
	PromotionReason   string   `yaml:"promotion_reason"`
	SourceRefs        []string `yaml:"source_refs"`
	GeneratedFrom     []string `yaml:"generated_from"`
}

// entryToMap renders a catalog entry as id + title + sorted Fields (yaml.v3
// sorts map keys, giving deterministic output).
func entryToMap(e domain.CatalogEntry) map[string]string {
	m := make(map[string]string, len(e.Fields)+2)
	for k, v := range e.Fields {
		m[k] = v
	}
	m["id"] = e.ID
	if e.Title != "" {
		m["title"] = e.Title
	}
	return m
}

func marshalSection(header string, v any) ([]byte, error) {
	var buf bytes.Buffer
	buf.WriteString(generatedHeader)
	buf.WriteString("# " + header + "\n")
	enc := yaml.NewEncoder(&buf)
	enc.SetIndent(2)
	if err := enc.Encode(v); err != nil {
		return nil, err
	}
	if err := enc.Close(); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

// Render serializes a bundle into the deterministic file map (name → content).
// Same bundle → identical bytes (sorted entries, sorted map keys, no timestamps).
func Render(b *Bundle) (map[string][]byte, error) {
	out := map[string][]byte{}
	mk := func(name, header string, entries []domain.CatalogEntry) error {
		rows := make([]map[string]string, len(entries))
		for i, e := range entries {
			rows[i] = entryToMap(e)
		}
		data, err := marshalSection(header, rows)
		if err != nil {
			return fmt.Errorf("render %s: %w", name, err)
		}
		out[name] = data
		return nil
	}
	if err := mk(FileAuthorities, "generated authority catalog", b.Authorities); err != nil {
		return nil, err
	}
	if err := mk(FileConditions, "generated condition catalog", b.Conditions); err != nil {
		return nil, err
	}
	if err := mk(FileForbiddenMoves, "generated forbidden-move catalog (each paired with a generative behavior)", b.ForbiddenMoves); err != nil {
		return nil, err
	}
	if err := mk(FileRequiredEvidence, "generated required-evidence catalog", b.RequiredEvidence); err != nil {
		return nil, err
	}

	ps := make([]emitPrinciple, len(b.Principles))
	for i, p := range b.Principles {
		ps[i] = emitPrinciple{
			ID: p.ID, Title: p.Title, AppliesWhen: p.AppliesWhen, Authorities: p.Authorities,
			RequiredEvidence: p.RequiredEvidence, ForbiddenMoves: p.ForbiddenMoves,
			RecommendedAction: p.RecommendedAction, RiskLevel: p.RiskLevel,
			RevocationRule: p.RevocationRule, PromotionReason: p.PromotionReason,
			SourceRefs: p.SourceRefs, GeneratedFrom: p.GeneratedFrom,
		}
	}
	pdata, err := marshalSection("generated proposed principles (PROPOSED only — promotion stays gated)", ps)
	if err != nil {
		return nil, err
	}
	out[FilePrinciples] = pdata

	sdata, err := marshalSection("generated source provenance index", b.Sources)
	if err != nil {
		return nil, err
	}
	out[FileSources] = sdata
	return out, nil
}

// GenerateToDir compiles the corpus and writes the deterministic generated bundle
// into outDir. It is the entry point used by the cmd/opscompile tool.
func GenerateToDir(corpusDir, outDir string) error {
	b, err := Compile(corpusDir)
	if err != nil {
		return err
	}
	files, err := Render(b)
	if err != nil {
		return err
	}
	if err := os.MkdirAll(outDir, 0o755); err != nil {
		return err
	}
	for name, data := range files {
		if err := os.WriteFile(filepath.Join(outDir, name), data, 0o644); err != nil {
			return fmt.Errorf("write %s: %w", name, err)
		}
	}
	// Also emit the flat-recall artifact (distinct from the behavioral catalogs)
	// that ai-memory self-seeds into the memories table at startup.
	recall, err := RenderRecall(corpusDir)
	if err != nil {
		return fmt.Errorf("render recall: %w", err)
	}
	if err := os.WriteFile(filepath.Join(outDir, FileRecall), recall, 0o644); err != nil {
		return fmt.Errorf("write %s: %w", FileRecall, err)
	}
	return nil
}
