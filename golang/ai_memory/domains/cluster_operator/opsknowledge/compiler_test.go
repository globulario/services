const fixtureCorpus = "testdata/corpus"

func compileFixture(t *testing.T) *Bundle {
	t.Helper()
	b, err := Compile(fixtureCorpus)
	if err != nil {
		t.Fatalf("Compile: %v", err)
	}
	return b
}

func hasEntry(es []domain.CatalogEntry, id string) (domain.CatalogEntry, bool) {
	for _, e := range es {
		if e.ID == id {
			return e, true
		}
	}
	return domain.CatalogEntry{}, false
}

func hasPrinciple(ps []domain.PrincipleSeed, id string) (domain.PrincipleSeed, bool) {
	for _, p := range ps {
		if p.ID == id {
			return p, true
		}
	}
	return domain.PrincipleSeed{}, false
}

// Each input class produces its expected ontology objects.
func TestCompileExtractsEachInputClass(t *testing.T) {
	b := compileFixture(t)

	// service-role → authority (+ owned_state field)
	if a, ok := hasEntry(b.Authorities, "authority.cluster.test_service.runtime_state"); !ok {
		t.Error("service-role did not produce an authority")
	} else if a.Fields["owned_state"] == "" || a.Fields["owner_kind"] != "service_rpc" {
		t.Errorf("authority missing owned_state/owner_kind: %+v", a.Fields)
	}

	// stage → lifecycle condition
	if _, ok := hasEntry(b.Conditions, "condition.cluster.lifecycle.test_stage"); !ok {
		t.Error("stage did not produce a lifecycle condition")
	}

	// runbook → required evidence (from success_criteria) + forbidden (from warning, at execute=step_2)
	if _, ok := hasEntry(b.RequiredEvidence, "evidence.cluster.test_runbook.success_criteria_met"); !ok {
		t.Error("runbook success_criteria did not produce required evidence")
	}
	if fm, ok := hasEntry(b.ForbiddenMoves, "forbidden.cluster.test_runbook.step_2"); !ok {
		t.Error("runbook warning did not produce a forbidden move")
	} else if fm.Fields["recommended_behavior"] == "" {
		t.Error("runbook-derived forbidden move has no generative pairing")
	}

	// incident → forbidden move
	if _, ok := hasEntry(b.ForbiddenMoves, "forbidden.cluster.incident.test_incident"); !ok {
		t.Error("incident did not produce a forbidden move")
	}

	// runbook (full OPEV) + service-role + incident → the 3 generative principles
	for _, id := range []string{
		"principle.cluster.observe_plan_execute_verify_before_recovery_claim",
		"principle.cluster.preserve_authority_boundary_during_repair",
		"principle.cluster.diagnostic_finding_is_claim_not_authority",
	} {
		if _, ok := hasPrinciple(b.Principles, id); !ok {
			t.Errorf("missing generative principle %q", id)
		}
	}
}

// Every generated forbidden move has a paired positive behavior (mandatory).
func TestGeneratedForbiddenMovesArePaired(t *testing.T) {
	b := compileFixture(t)
	if len(b.ForbiddenMoves) == 0 {
		t.Fatal("no forbidden moves generated")
	}
	for _, fm := range b.ForbiddenMoves {
		if fm.Fields["recommended_behavior"] == "" && fm.Fields["safe_next_step"] == "" {
			t.Errorf("forbidden move %q is restrictive-only (no generative pairing)", fm.ID)
		}
	}
}

// Every generated principle carries source_refs and generated_from lineage.
func TestGeneratedPrinciplesHaveLineage(t *testing.T) {
	b := compileFixture(t)
	if len(b.Principles) == 0 {
		t.Fatal("no principles generated")
	}
	for _, p := range b.Principles {
		if len(p.SourceRefs) == 0 {
			t.Errorf("principle %q missing source_refs", p.ID)
		}
		if len(p.GeneratedFrom) == 0 {
			t.Errorf("principle %q missing generated_from", p.ID)
		}
		if p.RecommendedAction == "" {
			t.Errorf("principle %q missing generative recommended_action", p.ID)
		}
	}
}

// Compilation is deterministic: same corpus → identical rendered bytes.
func TestCompileDeterministic(t *testing.T) {
	b1, err := Compile(fixtureCorpus)
	if err != nil {
		t.Fatal(err)
	}
	b2, err := Compile(fixtureCorpus)
	if err != nil {
		t.Fatal(err)
	}
	r1, err := Render(b1)
	if err != nil {
		t.Fatal(err)
	}
	r2, err := Render(b2)
	if err != nil {
		t.Fatal(err)
	}
	if len(r1) != len(r2) {
		t.Fatalf("file count differs: %d vs %d", len(r1), len(r2))
	}
	for name, data := range r1 {
		if !bytes.Equal(data, r2[name]) {
			t.Errorf("non-deterministic output for %s", name)
		}
	}
	// Every rendered file carries the DO-NOT-EDIT header.
	for name, data := range r1 {
		if !bytes.HasPrefix(data, []byte("# Code generated by operational knowledge compiler; DO NOT EDIT.")) {
			t.Errorf("%s missing DO NOT EDIT header", name)
		}
	}
}

// Sources carry stable refs + hashes (provenance preserved).
func TestSourcesHaveRefAndHash(t *testing.T) {
	b := compileFixture(t)
	if len(b.Sources) == 0 {
		t.Fatal("no sources recorded")
	}
	for _, s := range b.Sources {
		if s.Ref == "" || s.Hash == "" || s.Path == "" {
			t.Errorf("source missing ref/hash/path: %+v", s)
		}
	}
}

// findRepoPath walks up from the test's working dir to locate the repo root that
// contains docs/operational-knowledge — this also proves the compiler "detects
// the real operational-knowledge path".
func findRepoPath(t *testing.T) (corpus, generated string) {
	t.Helper()
	dir, err := os.Getwd()
	if err != nil {
		t.Fatal(err)
	}
	for i := 0; i < 12; i++ {
		c := filepath.Join(dir, "docs", "operational-knowledge")
		if fi, err := os.Stat(c); err == nil && fi.IsDir() {
			return c, filepath.Join(dir, "golang", "ai_memory", "domains", "cluster_operator", "generated")
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			break
		}
		dir = parent
	}
	t.Skip("docs/operational-knowledge not found from test working dir — skipping real-corpus golden test")
	return "", ""
}

// Golden test: the committed generated/ files must byte-match a fresh compile of
// the real corpus (deterministic, never hand-edited). Also proves real-corpus
// extraction produces the 3 generative principles.
func TestRealCorpusGoldenMatch(t *testing.T) {
	corpusDir, genDir := findRepoPath(t)
	b, err := Compile(corpusDir)
	if err != nil {
		t.Fatalf("Compile(real corpus): %v", err)
	}
	files, err := Render(b)
	if err != nil {
		t.Fatal(err)
	}
	for name, want := range files {
		got, err := os.ReadFile(filepath.Join(genDir, name))
		if err != nil {
			t.Fatalf("committed %s missing — run opscompile: %v", name, err)
		}
		if !bytes.Equal(got, want) {
			t.Errorf("committed %s is stale/hand-edited — regenerate with opscompile", name)
		}
	}
	for _, id := range []string{
		"principle.cluster.observe_plan_execute_verify_before_recovery_claim",
		"principle.cluster.preserve_authority_boundary_during_repair",
		"principle.cluster.diagnostic_finding_is_claim_not_authority",
	} {
		if _, ok := hasPrinciple(b.Principles, id); !ok {
			t.Errorf("real corpus did not produce %q", id)
		}
	}
}
