package main

import (
	"encoding/json"
	"fmt"
	"path/filepath"
	"strings"
)

// defaultResticExcludes is the list of paths restic must never read during a
// cluster backup. Three classes:
//
//   - circular: backup output paths that would include the backup itself.
//   - double-coverage: paths owned by another backup provider (etcd / minio).
//     Reading them via restic produces either corrupt copies (live etcd WAL)
//     or duplicate data that bloats the snapshot.
//   - cache / runtime / local-side-effects: regenerated automatically by
//     node_agent or cluster_controller; restoring stale copies is worse than
//     letting them regenerate. Local-backup files (e.g. yaml.bak.*) pollute
//     the capsule with operator artifacts.
//
// Restic accepts glob patterns in --exclude; the glob entries below rely on
// that behaviour (plan-*.json, *.bak.*, *.before-*).
var defaultResticExcludes = []string{
	// circular
	"/var/lib/globular/backups",
	"/var/backups/globular/restic",
	// covered by the etcd snapshot provider (and unsafe to copy live WAL)
	"/var/lib/globular/etcd",
	// covered by the minio provider via rclone sync
	"/var/lib/globular/minio/data",
	// package extract cache — regenerated by node_agent from the repository
	"/var/lib/globular/staging",
	// runtime state — regenerated by cluster_controller / node_agent
	"/var/lib/globular/nodeagent/state.json",
	"/var/lib/globular/nodeagent/plan-*.json",
	// local file backups (operator artifacts, NOT cluster state)
	"/var/lib/globular/scylla-manager-agent/*.bak.*",
	"/var/lib/globular/scylla-manager-agent/*.before-*",
	// generic cache dir
	"/var/lib/globular/.cache",
}

// extraOptionalPatterns are paths NOT in the configured excludes that should
// still be treated as non-failing when restic reports a permission-denied.
// Anything outside this set AND outside the configured excludes is REQUIRED
// for restore correctness, and an unreadable result fails the backup with a
// clear error message.
var extraOptionalPatterns = []string{
	"/var/lib/globular/.bootstrap-*", // day-0 bootstrap artifacts; regenerable on bootstrap replay
	"*.tmp",                          // any temp file
}

// buildResticBackupArgs constructs the restic backup argv slice. Pure
// function with deterministic output — easy to read in code review, easy to
// test, easy to log.
//
// Argument order (read left-to-right):
//  1. "backup", "--json"
//  2. dataDir + repo as --exclude (preserves original placement, kept first
//     so an operator scanning restic stderr sees the most-relevant excludes
//     near the top of the arg list).
//  3. defaultResticExcludes as --exclude (skipping any that exactly match
//     dataDir/repo, to keep the arg list tidy and the manifest accurate).
//  4. extraExcludes as --exclude (caller-supplied, appended last so they
//     never clobber defaults nor get reordered into the middle of them).
//  5. paths (positional — restic requires sources at the end).
func buildResticBackupArgs(repo, dataDir string, paths, extraExcludes []string) []string {
	args := []string{"backup", "--json"}
	if dataDir != "" {
		args = append(args, "--exclude", dataDir)
	}
	if repo != "" {
		args = append(args, "--exclude", repo)
	}
	for _, ex := range defaultResticExcludes {
		if ex == dataDir || ex == repo {
			continue
		}
		args = append(args, "--exclude", ex)
	}
	for _, ex := range extraExcludes {
		ex = strings.TrimSpace(ex)
		if ex == "" {
			continue
		}
		args = append(args, "--exclude", ex)
	}
	args = append(args, paths...)
	return args
}

// resticErrorLine matches restic's JSON-per-line stderr format. Only the
// fields we read are declared.
type resticErrorLine struct {
	MessageType string `json:"message_type"`
	Error       struct {
		Message string `json:"message"`
	} `json:"error"`
	During string `json:"during"`
	Item   string `json:"item"`
	Code   int    `json:"code"`
}

// parseResticErrorItems extracts the unique `item` path out of each
// message_type=error line in stderr. Lines that don't parse as JSON or
// aren't error rows are skipped. Order is first-seen.
func parseResticErrorItems(stderr string) []string {
	seen := map[string]bool{}
	var out []string
	for _, line := range strings.Split(stderr, "\n") {
		line = strings.TrimSpace(line)
		if line == "" || !strings.HasPrefix(line, "{") {
			continue
		}
		var rec resticErrorLine
		if err := json.Unmarshal([]byte(line), &rec); err != nil {
			continue
		}
		if rec.MessageType != "error" || rec.Item == "" {
			continue
		}
		if seen[rec.Item] {
			continue
		}
		seen[rec.Item] = true
		out = append(out, rec.Item)
	}
	return out
}

// matchesExcludePattern reports whether path is "covered" by an exclude
// pattern. Semantics align with what we actually pass to restic via
// --exclude:
//   - exact path match
//   - filepath.Match on the full path (handles patterns like plan-*.json
//     when paired with a directory prefix in the pattern itself)
//   - filepath.Match on the basename — restic's exclude treats *.bak as
//     matching any file with that suffix, regardless of directory
//   - directory prefix: when the pattern contains no glob meta and the
//     path starts with `pattern + "/"`, the path is inside the excluded dir
func matchesExcludePattern(path, pattern string) bool {
	if path == pattern {
		return true
	}
	if strings.ContainsAny(pattern, "*?[") {
		if ok, _ := filepath.Match(pattern, path); ok {
			return true
		}
		if ok, _ := filepath.Match(pattern, filepath.Base(path)); ok {
			return true
		}
		return false
	}
	return strings.HasPrefix(path, pattern+"/")
}

// isOptionalUnreadable reports whether an unreadable path should be treated
// as non-failing. True when the path matches a configured exclude OR a
// built-in optional pattern. False means the path is required for restore
// and missing data is a real problem.
func isOptionalUnreadable(path string, configuredExcludes []string) bool {
	for _, ex := range configuredExcludes {
		if matchesExcludePattern(path, ex) {
			return true
		}
	}
	for _, p := range extraOptionalPatterns {
		if matchesExcludePattern(path, p) {
			return true
		}
	}
	return false
}

// classifyResticWarnings parses restic's JSON stderr and returns the
// unreadable paths that are REQUIRED for restore. allOptional is true when
// every parsed error item is covered by configuredExcludes or by a
// built-in optional pattern — i.e. exit-code 3 represents only noise and
// the backup verdict can remain "success".
//
// configuredExcludes should be the exact list passed to restic via
// --exclude (defaults + caller extras), so the classifier mirrors what
// restic actually skipped.
func classifyResticWarnings(stderr string, configuredExcludes []string) (missingRequired []string, allOptional bool) {
	items := parseResticErrorItems(stderr)
	allOptional = true
	for _, item := range items {
		if isOptionalUnreadable(item, configuredExcludes) {
			continue
		}
		missingRequired = append(missingRequired, item)
		allOptional = false
	}
	return missingRequired, allOptional
}

// evaluateResticResult turns a restic exit code + stderr into a backup
// verdict. Pure function; the runResticBackup glue calls this and acts on
// the result. Side effect: enriches outputs with "unreadable_required"
// when applicable (caller passes the map; nil-safe).
//
// Exit code semantics (per restic docs):
//   - 0: backup complete, no warnings.
//   - 1: fatal error (e.g. cannot reach repo). Caller already fails on this.
//   - 3: warnings during scan/archival; snapshot WAS still created. We
//     treat this as success ONLY when classifyResticWarnings reports
//     allOptional. Otherwise we fail with a clear message naming the
//     missing required paths so the operator knows the backup is
//     incomplete.
//
// For any other non-zero exit code, returns (false, "") so the caller can
// apply its own fail-with-stderr-detail handling.
func evaluateResticResult(exitCode int, stderr string, configuredExcludes []string, outputs map[string]string) (success bool, failMsg string) {
	if exitCode == 0 {
		return true, ""
	}
	if exitCode != 3 {
		return false, ""
	}
	missing, allOptional := classifyResticWarnings(stderr, configuredExcludes)
	if allOptional {
		return true, ""
	}
	if outputs != nil {
		outputs["unreadable_required"] = strings.Join(missing, ",")
	}
	sample := missing
	if len(sample) > 5 {
		sample = sample[:5]
	}
	return false, fmt.Sprintf(
		"restic backup failed: required path(s) unreadable by backup_manager user=globular: %s (total %d; full list in outputs[\"unreadable_required\"])",
		strings.Join(sample, ", "), len(missing))
}
