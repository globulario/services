package versionutil

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

// TestGeneratedVersionFilesContract enforces the repository contract for
// service zz_version_generated.go files:
// - generated provenance header exists
// - Version is a var (not const)
// - init guard rejects empty version
func TestGeneratedVersionFilesContract(t *testing.T) {
	root := repoRoot(t)
	var files []string
	err := filepath.Walk(filepath.Join(root, "golang"), func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		if info == nil || info.IsDir() {
			return nil
		}
		if strings.HasSuffix(path, "zz_version_generated.go") {
			files = append(files, path)
		}
		return nil
	})
	if err != nil {
		t.Fatalf("walk failed: %v", err)
	}
	if len(files) == 0 {
		t.Skip("no generated version files found; run golang/build/gen-version.sh before release builds")
	}

	for _, p := range files {
		b, err := os.ReadFile(p)
		if err != nil {
			t.Fatalf("read %s: %v", p, err)
		}
		s := string(b)
		rel, _ := filepath.Rel(root, p)

		if !strings.Contains(s, "Code generated by build/gen-version.sh") {
			t.Fatalf("%s: missing generated provenance header", rel)
		}
		if strings.Contains(s, "const Version") {
			t.Fatalf("%s: Version must be var, not const (ldflags override contract)", rel)
		}
		if !strings.Contains(s, "var Version = ") {
			t.Fatalf("%s: missing `var Version = ...` declaration", rel)
		}
		if !strings.Contains(s, `if Version == ""`) {
			t.Fatalf("%s: missing empty-version runtime guard", rel)
		}
	}
}

func repoRoot(t *testing.T) string {
	t.Helper()
	wd, err := os.Getwd()
	if err != nil {
		t.Fatalf("getwd: %v", err)
	}
	// Test runs from golang/versionutil package; repo root is ../..
	return filepath.Clean(filepath.Join(wd, "..", ".."))
}
