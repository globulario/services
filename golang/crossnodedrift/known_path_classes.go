package crossnodedrift

// known_path_classes.go — the registry of cluster-visible file paths
// Globular tracks, and the authority that decides canonical content
// for each. Adding a path here is the cheap way to bring a new class
// of files under doctor's drift radar; forgetting to add one is the
// expensive way (the webroot bug). Every class MUST declare an
// authority — leaving Authority = AuthorityUndefined intentionally
// raises FindingAuthorityUndefined so the omission is loud, not
// silent.

// PathClassWebroot is the static site / SPA content served from
// objectstore but cached locally by gateways. Operators expect the
// same files everywhere; the two-day drift incident that motivated
// Phase 7 lived here.
var PathClassWebroot = PathClass{
	Name:        "webroot",
	Authority:   AuthorityObjectstoreBacked,
	Description: "Web-rooted static assets served by gateways. Objectstore is the truth; local copies are caches.",
}

// PathClassPublicAppAssets are the per-application web assets
// (index.html, JS bundles) that ApplicationRelease ships. Authoritative
// content lives in objectstore; nodes hold caches.
var PathClassPublicAppAssets = PathClass{
	Name:        "public_app_assets",
	Authority:   AuthorityObjectstoreBacked,
	Description: "Per-application web assets shipped by ApplicationRelease. Objectstore is the truth.",
}

// PathClassServiceSpecs are the deploy-time YAML / JSON specs that
// drive systemd unit rendering. Spec content is generated at deploy
// time from the package + the controller's desired state.
var PathClassServiceSpecs = PathClass{
	Name:        "service_specs",
	Authority:   AuthorityGeneratedFromRepository,
	Description: "Per-service spec files written under /var/lib/globular/specs. Regenerated from the installed package on every deploy.",
}

// PathClassRenderedSystemdUnits are the .service files under
// /etc/systemd/system/globular-*.service. Content is generated by the
// deploy step from etcd-resident spec + cluster state.
var PathClassRenderedSystemdUnits = PathClass{
	Name:        "rendered_systemd_units",
	Authority:   AuthorityGeneratedFromEtcd,
	Description: "/etc/systemd/system/globular-*.service. Generated from etcd-resident specs at deploy time. Phase 5b detects effective-vs-rendered drift; Phase 7 detects per-node disagreement.",
}

// PathClassObjectstoreConfig is the per-node MinIO env / topology
// config. Authority is the controller, which renders it from etcd.
var PathClassObjectstoreConfig = PathClass{
	Name:        "objectstore_config",
	Authority:   AuthorityGeneratedFromEtcd,
	Description: "Per-node MinIO env file. Rendered from etcd objectstore topology by the controller.",
}

// PathClassEnvoyXdsSnapshots are the xDS rendered snapshots Envoy
// loads at startup and the controller refreshes on cluster changes.
var PathClassEnvoyXdsSnapshots = PathClass{
	Name:        "envoy_xds_snapshots",
	Authority:   AuthorityGeneratedFromEtcd,
	Description: "Envoy xDS resource snapshots under /var/lib/globular/xds/. Rendered from etcd-resident routes/clusters/listeners.",
}

// PathClassCertSANFiles are the TLS material under
// /var/lib/globular/pki and /var/lib/globular/domains. Each domain's
// fullchain.pem must hash-match across all gateway nodes.
var PathClassCertSANFiles = PathClass{
	Name:        "cert_san_files",
	Authority:   AuthorityReplicated,
	Description: "TLS certificate material under /var/lib/globular/pki and /var/lib/globular/domains/*. Every gateway must hold an identical copy.",
}

// PathClassPackageInstalledBinaries are the installed service binaries
// under /usr/lib/globular/bin/*. Each binary's hash must match the
// repository-published checksum for the installed build_id.
var PathClassPackageInstalledBinaries = PathClass{
	Name:        "package_installed_binaries",
	Authority:   AuthorityGeneratedFromRepository,
	Description: "Installed service binaries under /usr/lib/globular/bin/*. Each must match the repository-published checksum for the installed build_id (Phase 1 verifies at apply time; Phase 7 catches per-node disagreement).",
}

// KnownPathClasses returns every declared class in stable Name order.
// Used by doctor / verifier code that wants to iterate the catalog.
func KnownPathClasses() []PathClass {
	return []PathClass{
		PathClassCertSANFiles,
		PathClassEnvoyXdsSnapshots,
		PathClassObjectstoreConfig,
		PathClassPackageInstalledBinaries,
		PathClassPublicAppAssets,
		PathClassRenderedSystemdUnits,
		PathClassServiceSpecs,
		PathClassWebroot,
	}
}

// LookupPathClass returns the class with the given Name, or
// PathClass{Authority: AuthorityUndefined} when the name is unknown.
// Callers should pass the result through DetectDrift; the undefined
// authority will raise FindingAuthorityUndefined cleanly rather than
// silently passing.
func LookupPathClass(name string) PathClass {
	for _, c := range KnownPathClasses() {
		if c.Name == name {
			return c
		}
	}
	return PathClass{Name: name, Authority: AuthorityUndefined}
}
