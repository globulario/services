package enforce

import (
	"os"
	"path/filepath"
	"strings"

	"github.com/globulario/services/golang/awareness/fixledger"
)

// ScaffoldScanResult holds counts and findings from a scaffold TODO-skip scan.
type ScaffoldScanResult struct {
	TotalScaffoldSkips           int
	DoneFixcasesWithScaffoldOnly int
	Findings                     []Finding
	// Unverified is true when ScanScaffoldTests was called without a usable
	// repoRoot (e.g. production MCP host with no source on disk). Callers
	// must treat this as a telemetry-unavailable signal — NOT as evidence
	// that there are zero scaffold skips. See
	// awareness.source_scan_requires_verified_repo_root and the 2026-05-14
	// composed-path failure entry.
	Unverified       bool
	UnverifiedReason string
}

// scaffoldMarkers are the patterns that identify a generated scaffold TODO skip.
var scaffoldMarkers = []string{
	`t.Skip("TODO: implement required awareness test")`,
	`t.Skip("TODO")`,
	`t.Skipf("TODO`,
	`// Code generated by awareness scaffolder`,
	`// required awareness test scaffold`,
}

// ScanScaffoldTests walks all *_test.go files under repoRoot, counts
// t.Skip("TODO") scaffold patterns, and cross-references DONE fix cases in
// docsDir/fix_cases.yaml to find cases verified only by scaffold stubs.
//
// docsDir may be "" — the DONE fix-case cross-reference is skipped but
// scaffold skip counting still runs.
//
// When repoRoot is "", returns a result with Unverified=true (no source
// to scan, this is the expected production-MCP case). Do NOT silently
// return a zero-count result that downstream consumers will read as
// "no scaffolds found."
func ScanScaffoldTests(repoRoot, docsDir string) ScaffoldScanResult {
	var res ScaffoldScanResult

	if repoRoot == "" {
		res.Unverified = true
		res.UnverifiedReason = "repo root not provided — cannot scan for scaffold tests"
		return res
	}

	// Build a set of test function names that are scaffold TODO skips.
	scaffoldFuncs := map[string]bool{} // funcName → true

	_ = filepath.Walk(repoRoot, func(path string, info os.FileInfo, err error) error {
		if err != nil || info.IsDir() {
			return nil
		}
		rel, _ := filepath.Rel(repoRoot, path)
		if isExcludedPath(rel) || !strings.HasSuffix(path, "_test.go") {
			return nil
		}
		data, readErr := os.ReadFile(path)
		if readErr != nil {
			return nil
		}
		content := string(data)
		funcs := extractScaffoldFuncs(content)
		for _, fn := range funcs {
			res.TotalScaffoldSkips++
			scaffoldFuncs[fn] = true
			res.Findings = append(res.Findings, Finding{
				Code:     CodeScaffoldTodoSkip,
				Severity: SeverityWarning,
				File:     rel,
				Symbol:   fn,
				Message:  "test function is a scaffold TODO skip — not real verification",
			})
		}
		return nil
	})

	// Cross-reference DONE fix cases.
	if docsDir != "" {
		fixCasesPath := filepath.Join(docsDir, "fix_cases.yaml")
		cases, err := fixledger.LoadFixCases(fixCasesPath)
		if err == nil {
			for _, fc := range cases {
				if fc.Status != fixledger.FixDone || len(fc.RequiredTests) == 0 {
					continue
				}
				// A DONE fix case is scaffold-only if ALL required tests are scaffold stubs.
				allScaffold := true
				for _, t := range fc.RequiredTests {
					name := strings.TrimSpace(t)
					if idx := strings.IndexByte(name, ' '); idx >= 0 {
						name = name[:idx]
					}
					if !scaffoldFuncs[name] {
						allScaffold = false
						break
					}
				}
				if allScaffold {
					res.DoneFixcasesWithScaffoldOnly++
					res.Findings = append(res.Findings, Finding{
						Code:     CodeDoneFixcaseScaffoldOnly,
						Severity: SeverityError,
						Symbol:   fc.ID,
						Message:  "DONE fix case '" + fc.ID + "' is verified only by scaffold TODO skips — not real verification",
					})
				}
			}
		}
	}

	return res
}

// extractScaffoldFuncs returns the names of test functions in content that
// contain one of the scaffold markers.
func extractScaffoldFuncs(content string) []string {
	var out []string
	lines := strings.Split(content, "\n")
	var currentFunc string
	for _, line := range lines {
		trimmed := strings.TrimSpace(line)
		if strings.HasPrefix(trimmed, "func Test") && strings.Contains(trimmed, "(t *testing.T)") {
			// Extract function name.
			rest := strings.TrimPrefix(trimmed, "func ")
			if idx := strings.IndexByte(rest, '('); idx > 0 {
				currentFunc = rest[:idx]
			}
			continue
		}
		if currentFunc != "" {
			for _, marker := range scaffoldMarkers {
				if strings.Contains(trimmed, marker) {
					out = append(out, currentFunc)
					currentFunc = ""
					break
				}
			}
		}
		// Reset on closing brace at top level.
		if trimmed == "}" {
			currentFunc = ""
		}
	}
	return out
}

// isExcludedPath returns true for paths that should not be scanned.
func isExcludedPath(rel string) bool {
	for _, seg := range strings.Split(rel, string(filepath.Separator)) {
		switch seg {
		case "vendor", ".git", "node_modules", "dist", "build", ".cache":
			return true
		}
	}
	return false
}
