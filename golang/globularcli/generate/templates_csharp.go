package generate

import "fmt"

// CSharpTemplates returns all C# templates for service generation.
func CSharpTemplates(data *ServiceData) map[string]string {
	templates := map[string]string{
		"Program.cs":                                          csharpProgramTemplate,
		fmt.Sprintf("%s.Host.csproj", data.ServiceName):       csharpCsprojTemplate,
		"appsettings.json":                                    csharpAppsettingsTemplate,
		fmt.Sprintf("%s.manifest.json", toSnakeCase(data.PackageName)): csharpManifestTemplate,
	}

	if !data.SkipSpec {
		for k, v := range SpecTemplate(data, "csharp") {
			templates[k] = v
		}
	}

	return templates
}

