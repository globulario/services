package generate

import "fmt"

// GoTemplates returns all Go templates for a full service generation (server + client + tests).
func GoTemplates(data *ServiceData) map[string]string {
	templates := GoServerTemplates(data)

	// Client
	clientDir := fmt.Sprintf("%s_client", data.PackageName)
	templates[fmt.Sprintf("%s/%s_client.go", clientDir, data.PackageName)] = goClientTemplate

	// Tests
	serverDir := fmt.Sprintf("%s_server", data.PackageName)
	templates[fmt.Sprintf("%s/%s_test.go", serverDir, data.PackageName)] = goTestTemplate

	return templates
}

// GoServerTemplates returns only server-side Go templates.
func GoServerTemplates(data *ServiceData) map[string]string {
	serverDir := fmt.Sprintf("%s_server", data.PackageName)
	templates := map[string]string{
		fmt.Sprintf("%s/server.go", serverDir):   goServerTemplate,
		fmt.Sprintf("%s/config.go", serverDir):   goConfigTemplate,
		fmt.Sprintf("%s/handlers.go", serverDir): goHandlersTemplate,
	}

	if !data.SkipSpec {
		for k, v := range SpecTemplate(data, "go") {
			templates[k] = v
		}
	}

	return templates
}

