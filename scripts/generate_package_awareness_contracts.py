#!/usr/bin/env python3
"""Generate services-local AWG contracts from packages/*/awareness.yaml.

The packages repo owns the Globular-specific AwarenessContract schema. AWG can
import that schema natively, and this projection keeps the services awareness
self-audit self-contained when packages/metadata is not one of its input roots.
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Any

import yaml


HEADER = """# GENERATED by scripts/generate_package_awareness_contracts.py -- DO NOT EDIT.
# Source: packages/*/awareness.yaml
# Projection schema: AWG contracts; native package AwarenessContract import also exists.
version: "1"
schema: "globular.awareness.package_contracts/v1"

contracts:
"""


def scalar_list(values: Any) -> list[str]:
    if not isinstance(values, list):
        return []
    return [str(v).strip() for v in values if str(v).strip()]


def named_dependencies(values: Any) -> list[str]:
    if not isinstance(values, list):
        return []
    out: list[str] = []
    for item in values:
        if not isinstance(item, dict):
            continue
        service = str(item.get("service", "")).strip()
        required = item.get("required")
        phase = str(item.get("phase", "")).strip()
        if not service:
            continue
        label = service
        if phase:
            label = f"{label}@{phase}"
        if isinstance(required, bool):
            label = f"{label} required={str(required).lower()}"
        out.append(label)
    return out


def key_count(section: Any, key: str) -> int:
    if not isinstance(section, dict):
        return 0
    values = section.get(key)
    if not isinstance(values, list):
        return 0
    return len(values)


def contract_from(path: Path, packages_root: Path) -> dict[str, str]:
    raw = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    if raw.get("apiVersion") != "awareness.globular.io/v1" or raw.get("kind") != "AwarenessContract":
        raise ValueError(f"{path}: not an AwarenessContract")

    package = str(raw.get("package") or raw.get("service") or path.parent.name).strip()
    service = str(raw.get("service") or package).strip()
    package_kind = str(raw.get("package_kind") or "unknown").strip().lower()
    summary = " ".join(str(raw.get("summary") or "").split())

    owns = raw.get("owns")
    reads = raw.get("reads")
    writes = raw.get("writes")
    details = [
        f"package_kind={package_kind}",
        f"depends_on={', '.join(named_dependencies(raw.get('depends_on'))) or 'none'}",
        f"owns_etcd={key_count(owns, 'etcd_keys')}",
        f"writes_etcd={key_count(writes, 'etcd_keys')}",
        f"reads_etcd={key_count(reads, 'etcd_keys')}",
        f"invariants={', '.join(scalar_list(raw.get('invariants'))) or 'none'}",
        f"forbidden_fixes={', '.join(scalar_list(raw.get('forbidden_fixes'))) or 'none'}",
        f"required_tests={', '.join(scalar_list(raw.get('required_tests'))) or 'none'}",
        f"source={path.relative_to(packages_root).as_posix()}",
    ]

    return {
        "id": f"contract.package.{package}.awareness",
        "domain": "package_awareness",
        "service": service,
        "kind": package_kind,
        "summary": f"{summary} {'; '.join(details)}".strip(),
    }


def render_contract(item: dict[str, str]) -> str:
    dumped = yaml.safe_dump(
        [item],
        sort_keys=False,
        default_flow_style=False,
        width=100,
        allow_unicode=False,
    )
    return "\n".join(f"  {line}" if line else line for line in dumped.splitlines())


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--packages-root",
        default="../packages",
        help="path to the packages repository root",
    )
    parser.add_argument(
        "--output",
        default="docs/awareness/generated/package_awareness_contracts.yaml",
        help="generated AWG contracts file",
    )
    args = parser.parse_args()

    packages_root = Path(args.packages_root).resolve()
    output = Path(args.output)

    contracts = [
        contract_from(path, packages_root)
        for path in sorted(packages_root.glob("*/awareness.yaml"))
    ]

    body = "\n".join(render_contract(contract) for contract in contracts)
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(f"{HEADER}{body}\n", encoding="utf-8")
    print(f"wrote {output} ({len(contracts)} package contracts)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
