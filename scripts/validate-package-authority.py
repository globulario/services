#!/usr/bin/env python3
"""validate-package-authority.py — enforce the artifact/source authority matrix.

Authority model:
  - packages/registry.yaml is the only authored package identity authority.
  - packages/metadata/<name>/specs/*.yaml is the only canonical package recipe authority.
  - services/webroot is the authored release webroot source.
  - services/generated is disposable generated workspace only.
  - services/dist is disposable release output only.
  - globular-installer/internal/packagecatalog is a generated embedded package mirror.
  - globular-installer/internal/assets/webroot is a generated fallback mirror only.
  - globular-installer/internal/assets/bin is installer-private helper assets only.

Mirrors, staging directories, and outputs must never become source authority.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import os
from pathlib import Path
import re
import subprocess
import sys
from typing import Iterable

import yaml


RUNTIME_KINDS = {"service", "infrastructure"}
SPEC_SUFFIXES = ("_service.yaml", "_cmd.yaml", "_application.yaml")
MIRROR_HEADER = "# Generated mirror of packages/registry.yaml.\n# Do not edit this file directly.\n"
WEBROOT_MIRROR_SCHEMA = "globular.webroot.mirror/v1"
PACKAGECATALOG_SCHEMA = "globular.packagecatalog.mirror/v1"
LEGACY_SPEC_ROOTS = (Path("../specs/specs"),)
ALLOWED_GENERATED_SPECS_REFS = {
    "README-BUILD.md",
    "build-all-packages.sh",
    "golang/deploy/deploy.go",
    "golang/deploy/deploy_test.go",
    "golang/globularcli/pkgpack/packagespec_real_test.go",
    "golang/globularcli/tools/pkggen/pkggen.sh",
    "golang/globularcli/tools/specgen/specgen.sh",
    "scripts/validate-package-authority.py",
    "scripts/regenerate-release-inputs.sh",
}
ALLOWED_DIST_STAGING_REFS = {
    ".github/workflows/release.yml",
    "golang/build/detect-changes.py",
    "scripts/build-release.sh",
    "scripts/validate-package-authority.py",
}
ALLOWED_CANONICAL_SPEC_PREFIXES = (
    "packages/metadata/",
    "packages/specs/",
    "globular-installer/internal/packagecatalog/specs/",
    "globular-installer/internal/specs/",
    "services/generated/",
    "services/golang/generated/",
    "services/test/",
    "docs/legacy/",
    "testdata/",
)
ALLOWED_INSTALLER_ASSET_BIN_FILES = {".gitkeep", "check-ingress.sh"}
INSTALLER_STEP_CASE_RE = re.compile(r'case\s+"([^"]+)"\s*:')
SYSTEMD_INLINE_SHELL_PERCENT_RE = re.compile(r"Exec(?:Start|StartPre|StartPost|Reload|Stop|StopPost)=.*?/bin/sh -c.*(^|[^%])%(?!%)")


def sha256_file(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as f:
        for chunk in iter(lambda: f.read(65536), b""):
            h.update(chunk)
    return h.hexdigest()


def fail(errors: list[str], message: str) -> None:
    errors.append(message)
    print(f"ERROR: {message}", file=sys.stderr)


def warn(message: str) -> None:
    print(f"WARN: {message}", file=sys.stderr)


def rel(path: Path, root: Path) -> str:
    try:
        return str(path.relative_to(root))
    except ValueError:
        return str(path)


def rg_files(pattern: str, roots: list[str], cwd: Path) -> set[str]:
    try:
        proc = subprocess.run(
            ["rg", "-l", pattern, *roots],
            cwd=cwd,
            check=False,
            capture_output=True,
            text=True,
        )
    except FileNotFoundError:
        return set()
    return {line.strip() for line in proc.stdout.splitlines() if line.strip()}


def load_registry(path: Path) -> list[dict]:
    data = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    entries = data.get("packages", []) if isinstance(data, dict) else data
    if not isinstance(entries, list):
        raise ValueError(f"{path}: expected registry package list")
    return [entry for entry in entries if isinstance(entry, dict) and entry.get("name")]


def spec_files(spec_dir: Path) -> list[Path]:
    if not spec_dir.is_dir():
        return []
    return sorted(path for path in spec_dir.glob("*.yaml") if path.is_file())


def load_yaml(path: Path) -> dict:
    data = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    return data if isinstance(data, dict) else {}


def strip_mirror_header(text: str) -> str:
    return text[len(MIRROR_HEADER) :] if text.startswith(MIRROR_HEADER) else text


def declared_staged_binaries(registry_entries: list[dict], canonical_specs: dict[str, Path]) -> tuple[set[str], dict[str, set[str]]]:
    allowed: set[str] = set()
    by_package: dict[str, set[str]] = {}

    for entry in registry_entries:
        name = entry["name"]
        spec_path = canonical_specs.get(name)
        if spec_path is None:
            continue

        spec_doc = load_yaml(spec_path)
        meta = spec_doc.get("metadata") or {}
        declared: set[str] = set()

        registry_bin = str(entry.get("binary") or "").strip()
        if registry_bin:
            declared.add(os.path.basename(registry_bin))

        entrypoint = str(meta.get("entrypoint") or "").strip()
        if entrypoint:
            declared.add(os.path.basename(entrypoint))

        for extra in meta.get("extra_binaries") or []:
            extra_name = str(extra).strip()
            if extra_name:
                declared.add(os.path.basename(extra_name))

        declared = {bin_name for bin_name in declared if bin_name}
        if declared:
            by_package[name] = declared
            allowed.update(declared)

    return allowed, by_package


def check_legacy_roots(errors: list[str], services_root: Path) -> None:
    for legacy_root in LEGACY_SPEC_ROOTS:
        candidate = (services_root / legacy_root).resolve()
        if candidate.is_dir():
            fail(errors, f"legacy spec root present at {candidate} — duplicate active spec forests are forbidden")


def check_metadata_specs(
    errors: list[str],
    services_root: Path,
    packages_root: Path,
    registry_entries: list[dict],
) -> tuple[dict[str, Path], dict[str, dict]]:
    registry_by_name = {entry["name"]: entry for entry in registry_entries}
    metadata_root = packages_root / "metadata"
    spec_by_name: dict[str, Path] = {}

    if not metadata_root.is_dir():
        fail(errors, f"packages metadata root missing: {metadata_root}")
        return spec_by_name, registry_by_name

    for pkg_dir in sorted(path for path in metadata_root.iterdir() if path.is_dir()):
        name = pkg_dir.name
        if name not in registry_by_name:
            fail(errors, f"{rel(pkg_dir, services_root)} exists without a matching packages/registry.yaml entry")
            continue
        specs = spec_files(pkg_dir / "specs")
        if not specs:
            fail(errors, f"{rel(pkg_dir / 'specs', services_root)} missing — registry package {name} has no canonical spec path")
            continue
        if len(specs) != 1:
            rendered = ", ".join(rel(path, services_root) for path in specs)
            fail(errors, f"registry package {name} has {len(specs)} canonical specs ({rendered}); exactly one is required")
            continue
        spec_by_name[name] = specs[0]

    for name in sorted(registry_by_name):
        if name not in spec_by_name:
            pkg_dir = metadata_root / name
            if not pkg_dir.is_dir():
                fail(errors, f"registry package {name} has no metadata directory under {rel(metadata_root, services_root)}")

    return spec_by_name, registry_by_name


def expected_mirror_specs(registry_entries: Iterable[dict], canonical_specs: dict[str, Path]) -> dict[str, Path]:
    out: dict[str, Path] = {}
    for entry in registry_entries:
        name = entry["name"]
        if entry.get("kind") not in RUNTIME_KINDS:
            continue
        spec = canonical_specs.get(name)
        if spec is not None:
            out[spec.name] = spec
    return out


def check_installer_packagecatalog(
    errors: list[str],
    services_root: Path,
    installer_root: Path,
    registry_path: Path,
    registry_entries: list[dict],
    canonical_specs: dict[str, Path],
) -> None:
    legacy_root = installer_root / "internal" / "specs"
    mirror_root = installer_root / "internal" / "packagecatalog" / "specs"
    manifest_path = installer_root / "internal" / "packagecatalog" / "manifest.json"

    if not mirror_root.is_dir():
        fail(errors, f"installer packagecatalog mirror missing: {rel(mirror_root, services_root)}")
        return
    if legacy_root.exists():
        # Cross-repo transitional tolerance: some pinned installer refs still
        # carry the pre-rename embedded mirror under internal/specs. It is not
        # allowed to become authority, but its presence alone must not block the
        # services release workflow while the sibling repo is being migrated.
        warn(f"{rel(legacy_root, services_root)} still exists — tolerated as legacy embedded mirror because internal/packagecatalog/specs is present")

    expected = expected_mirror_specs(registry_entries, canonical_specs)
    actual = {path.name: path for path in spec_files(mirror_root)}
    for filename, source_path in sorted(expected.items()):
        target = actual.get(filename)
        if target is None:
            fail(errors, f"installer packagecatalog missing {rel(mirror_root / filename, services_root)} for registry package {source_path.parent.parent.name}")
            continue
        if strip_mirror_header(target.read_text(encoding="utf-8")) != source_path.read_text(encoding="utf-8"):
            fail(errors, f"installer packagecatalog drift: {rel(target, services_root)} does not match {rel(source_path, services_root)}")
    for filename, target in sorted(actual.items()):
        if filename not in expected:
            fail(errors, f"installer packagecatalog contains unregistered extra spec: {rel(target, services_root)}")

    if not manifest_path.is_file():
        warn(f"installer packagecatalog manifest missing: {rel(manifest_path, services_root)} — tolerated for pinned sibling repo until manifest lands upstream")
        return
    try:
        manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        fail(errors, f"{rel(manifest_path, services_root)} is not valid JSON: {exc}")
        return
    if manifest.get("schema") != PACKAGECATALOG_SCHEMA:
        fail(errors, f"{rel(manifest_path, services_root)} schema must be {PACKAGECATALOG_SCHEMA}")
    if manifest.get("source") != "packages/registry.yaml":
        fail(errors, f"{rel(manifest_path, services_root)} source must be packages/registry.yaml")
    if manifest.get("mirror_dir") != "globular-installer/internal/packagecatalog/specs":
        fail(errors, f"{rel(manifest_path, services_root)} mirror_dir must be globular-installer/internal/packagecatalog/specs")
    expected_digest = sha256_file(registry_path)
    if manifest.get("registry_sha256") != expected_digest:
        fail(errors, f"{rel(manifest_path, services_root)} registry_sha256 does not match current packages/registry.yaml")
    if manifest.get("package_count") != len(expected):
        fail(errors, f"{rel(manifest_path, services_root)} package_count={manifest.get('package_count')} does not match expected embedded count {len(expected)}")


def check_generated_specs_consumers(errors: list[str], services_root: Path) -> None:
    refs = rg_files("generated/specs", ["golang", "scripts", "build-all-packages.sh", "README.md", "README-BUILD.md"], services_root)
    for path in sorted(refs - ALLOWED_GENERATED_SPECS_REFS):
        fail(errors, f"{path} references services/generated/specs but is not an allowlisted generator/build helper")


def check_generated_workspace_inputs(errors: list[str], services_root: Path, registry_entries: list[dict]) -> None:
    generated_root = services_root / "generated"
    if not generated_root.is_dir():
        return
    registry_names = {entry["name"] for entry in registry_entries}
    for path in sorted(generated_root.glob("*.tgz")):
        base = path.name
        if not base.endswith("_linux_amd64.tgz"):
            fail(errors, f"{rel(path, services_root)} has unexpected generated package template name")
            continue
        stem = base[: -len("_linux_amd64.tgz")]
        parts = stem.rsplit("_", 1)
        if len(parts) != 2:
            fail(errors, f"{rel(path, services_root)} has unexpected generated package template naming format")
            continue
        pkg_name, _version = parts
        if pkg_name not in registry_names:
            fail(errors, f"{rel(path, services_root)} is not registry-backed; generated package inputs must be declared in packages/registry.yaml")


def check_release_staging_consumers(errors: list[str], services_root: Path) -> None:
    stale_bin_refs = rg_files(r"dist/bin", [".github", "golang", "scripts", "README.md", "README-BUILD.md"], services_root)
    for path in sorted(stale_bin_refs - ALLOWED_DIST_STAGING_REFS):
        fail(errors, f"{path} references services/dist/bin — release binary staging must live under services/dist/.staging/bin")

    staging_refs = rg_files(r"dist/\.staging/bin|dist/packages", [".github", "golang", "scripts"], services_root)
    for path in sorted(staging_refs - ALLOWED_DIST_STAGING_REFS):
        fail(errors, f"{path} consumes services/dist staging paths outside the release assembly allowlist")


def check_forbidden_spec_locations(errors: list[str], services_root: Path, packages_root: Path) -> None:
    forbidden_roots = [
        packages_root / "bin",
        packages_root / "dist",
        services_root / "dist",
    ]
    for root in forbidden_roots:
        if not root.exists():
            continue
        for path in root.rglob("*.yaml"):
            if path.name.endswith(SPEC_SUFFIXES):
                fail(errors, f"package spec found in forbidden artifact/staging directory: {rel(path, services_root)}")


def check_packages_bin_hygiene(
    errors: list[str],
    services_root: Path,
    packages_root: Path,
    declared_bins: set[str],
) -> None:
    packages_bin = packages_root / "bin"
    if not packages_bin.is_dir():
        return

    for path in sorted(p for p in packages_bin.iterdir() if p.is_file() or p.is_symlink()):
        if path.name not in declared_bins:
            fail(
                errors,
                f"{rel(path, services_root)} is not declared by packages/registry.yaml + packages/metadata/*/specs; "
                "packages/bin may contain only declared staged binaries",
            )


def check_required_staged_binaries(
    errors: list[str],
    services_root: Path,
    packages_root: Path,
    declared_bins: set[str],
    required_bins: list[str],
) -> None:
    packages_bin = packages_root / "bin"
    for bin_name in required_bins:
        if bin_name not in declared_bins:
            fail(
                errors,
                f"required staged binary {bin_name} is not declared by packages/registry.yaml + packages/metadata/*/specs",
            )
            continue
        if not (packages_bin / bin_name).is_file():
            fail(
                errors,
                f"{rel(packages_bin / bin_name, services_root)} missing — build/check context required staged binary {bin_name}",
            )


def check_duplicate_recipe_roots(errors: list[str], services_root: Path, packages_root: Path, installer_root: Path) -> None:
    search_roots = [packages_root, installer_root, services_root]
    for base in search_roots:
        for path in base.rglob("*.yaml"):
            if not path.name.endswith(SPEC_SUFFIXES):
                continue
            path_rel = rel(path, packages_root.parent)
            if path_rel.startswith(ALLOWED_CANONICAL_SPEC_PREFIXES):
                continue
            fail(errors, f"package recipe exists outside canonical or declared mirror paths: {path_rel}")


def check_webroot_mirror(errors: list[str], services_root: Path, installer_root: Path) -> None:
    source_root = services_root / "webroot"
    mirror_root = installer_root / "internal" / "assets" / "webroot"
    manifest_path = mirror_root / "manifest.json"

    if not source_root.is_dir():
        fail(errors, f"services webroot source missing: {rel(source_root, services_root)}")
        return
    if not mirror_root.is_dir():
        fail(errors, f"installer webroot mirror missing: {rel(mirror_root, services_root)}")
        return
    if not manifest_path.is_file():
        warn(f"installer webroot mirror manifest missing: {rel(manifest_path, services_root)} — tolerated for pinned sibling repo until mirror provenance lands upstream")
        return

    try:
        manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        fail(errors, f"{rel(manifest_path, services_root)} is not valid JSON: {exc}")
        return
    if manifest.get("schema") != WEBROOT_MIRROR_SCHEMA:
        fail(errors, f"{rel(manifest_path, services_root)} schema must be {WEBROOT_MIRROR_SCHEMA}")
    if manifest.get("source") != "services/webroot":
        fail(errors, f"{rel(manifest_path, services_root)} source must be services/webroot")
    if manifest.get("mirror_dir") != "globular-installer/internal/assets/webroot":
        fail(errors, f"{rel(manifest_path, services_root)} mirror_dir must be globular-installer/internal/assets/webroot")
    source_files = sorted(path for path in source_root.rglob("*") if path.is_file())
    mirror_files = sorted(path for path in mirror_root.rglob("*") if path.is_file() and path.name != "manifest.json")
    source_rel = {str(path.relative_to(source_root)): path for path in source_files}
    mirror_rel = {str(path.relative_to(mirror_root)): path for path in mirror_files}
    if set(source_rel) != set(mirror_rel):
        fail(errors, f"installer webroot mirror file set does not match services/webroot (source={sorted(source_rel)}, mirror={sorted(mirror_rel)})")
    files_manifest = manifest.get("files") or {}
    for rel_name, src_path in sorted(source_rel.items()):
        mirror_path = mirror_rel.get(rel_name)
        if mirror_path is None:
            continue
        src_digest = sha256_file(src_path)
        mirror_digest = sha256_file(mirror_path)
        if src_digest != mirror_digest:
            fail(errors, f"installer webroot mirror drift: {rel(mirror_path, services_root)} does not match {rel(src_path, services_root)}")
        if files_manifest.get(rel_name) != src_digest:
            fail(errors, f"{rel(manifest_path, services_root)} digest for {rel_name} is stale")


def check_installer_asset_bin(errors: list[str], services_root: Path, installer_root: Path, registry_entries: list[dict]) -> None:
    asset_bin_root = installer_root / "internal" / "assets" / "bin"
    if not asset_bin_root.is_dir():
        return
    registry_binaries = {str(entry.get("binary", "")).strip() for entry in registry_entries if entry.get("binary")}
    for path in sorted(asset_bin_root.iterdir()):
        if path.name in ALLOWED_INSTALLER_ASSET_BIN_FILES:
            continue
        if path.name in registry_binaries:
            fail(errors, f"{rel(path, services_root)} contains platform/package binary {path.name}; installer asset bin is for helper assets only")
            continue
        if path.is_file():
            raw = path.read_bytes()[:4]
            if raw.startswith(b"\x7fELF") or raw.startswith(b"MZ"):
                fail(errors, f"{rel(path, services_root)} contains a compiled binary; installer asset bin is for helper scripts only")
            elif path.suffix not in {".sh", ".txt", ".md", ".json"}:
                fail(errors, f"{rel(path, services_root)} is not a declared installer helper asset type")


def installer_supported_step_types(builder_path: Path) -> set[str]:
    text = builder_path.read_text(encoding="utf-8")
    return {match.group(1) for match in INSTALLER_STEP_CASE_RE.finditer(text)}


def check_installer_step_catalog(
    errors: list[str],
    services_root: Path,
    installer_root: Path,
    registry_entries: list[dict],
    canonical_specs: dict[str, Path],
) -> None:
    builder_path = installer_root / "pkg" / "installer" / "specplan_builder.go"
    if not builder_path.is_file():
        fail(errors, f"installer step catalogue source missing: {rel(builder_path, services_root)}")
        return

    supported_types = installer_supported_step_types(builder_path)
    if not supported_types:
        fail(errors, f"{rel(builder_path, services_root)} exposes no detectable installer step types")
        return

    registry_by_name = {entry["name"]: entry for entry in registry_entries}
    for pkg_name, spec_path in sorted(canonical_specs.items()):
        entry = registry_by_name.get(pkg_name)
        if not entry or entry.get("kind") not in RUNTIME_KINDS:
            continue

        spec_doc = load_yaml(spec_path)
        steps = spec_doc.get("steps") or []
        if not isinstance(steps, list):
            fail(errors, f"{rel(spec_path, services_root)} has invalid steps payload; expected a list")
            continue

        for index, step in enumerate(steps):
            if not isinstance(step, dict):
                fail(errors, f"{rel(spec_path, services_root)} step[{index}] is not a mapping")
                continue
            step_type = str(step.get("type") or "").strip()
            if not step_type:
                fail(errors, f"{rel(spec_path, services_root)} step[{index}] is missing type")
                continue
            if step_type not in supported_types:
                step_id = str(step.get("id") or f"step[{index}]")
                fail(
                    errors,
                    f"{rel(spec_path, services_root)} step {step_id!r} uses unsupported installer step type "
                    f"{step_type!r}; release/install packaging would drift from globular-installer/pkg/installer/specplan_builder.go",
                )


def check_canonical_unit_shell_safety(
    errors: list[str],
    services_root: Path,
    registry_entries: list[dict],
    canonical_specs: dict[str, Path],
) -> None:
    registry_by_name = {entry["name"]: entry for entry in registry_entries}
    for pkg_name, spec_path in sorted(canonical_specs.items()):
        entry = registry_by_name.get(pkg_name)
        if not entry or entry.get("kind") not in RUNTIME_KINDS:
            continue
        for lineno, line in enumerate(spec_path.read_text(encoding="utf-8").splitlines(), start=1):
            if SYSTEMD_INLINE_SHELL_PERCENT_RE.search(line):
                fail(
                    errors,
                    f"{rel(spec_path, services_root)}:{lineno} contains inline systemd shell with unescaped %; "
                    "systemd rewrites % sequences in Exec*= lines, so canonical specs must move that logic into a helper script",
                )


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--services-root", default=str(Path(__file__).resolve().parents[1]), help="path to the services repo root")
    parser.add_argument(
        "--require-staged-binary",
        action="append",
        default=[],
        help="require a declared staged binary to exist under packages/bin for this build/check context",
    )
    args = parser.parse_args()

    services_root = Path(args.services_root).resolve()
    repo_root = services_root.parent
    packages_root = (services_root / "../packages").resolve()
    installer_root = (services_root / "../globular-installer").resolve()
    registry_path = packages_root / "registry.yaml"

    if not packages_root.is_dir() or not registry_path.is_file():
        print("SKIP: packages repo or registry.yaml not available", file=sys.stderr)
        return 0
    if not installer_root.is_dir():
        print("SKIP: globular-installer repo not available", file=sys.stderr)
        return 0

    errors: list[str] = []
    check_legacy_roots(errors, services_root)
    try:
        registry_entries = load_registry(registry_path)
    except Exception as exc:  # noqa: BLE001
        fail(errors, f"cannot load {rel(registry_path, repo_root)}: {exc}")
        registry_entries = []

    canonical_specs, _registry_by_name = check_metadata_specs(errors, repo_root, packages_root, registry_entries)
    declared_bins, _declared_bins_by_package = declared_staged_binaries(registry_entries, canonical_specs)
    for entry in registry_entries:
        if entry.get("kind") in RUNTIME_KINDS and entry["name"] not in canonical_specs:
            fail(errors, f"registry runtime package {entry['name']} has no canonical metadata spec to mirror")

    check_installer_packagecatalog(errors, repo_root, installer_root, registry_path, registry_entries, canonical_specs)
    check_generated_specs_consumers(errors, services_root)
    check_generated_workspace_inputs(errors, services_root, registry_entries)
    check_release_staging_consumers(errors, services_root)
    check_forbidden_spec_locations(errors, repo_root, packages_root)
    check_packages_bin_hygiene(errors, repo_root, packages_root, declared_bins)
    check_required_staged_binaries(errors, repo_root, packages_root, declared_bins, args.require_staged_binary)
    check_duplicate_recipe_roots(errors, services_root, packages_root, installer_root)
    check_webroot_mirror(errors, services_root, installer_root)
    check_installer_asset_bin(errors, repo_root, installer_root, registry_entries)
    check_installer_step_catalog(errors, repo_root, installer_root, registry_entries, canonical_specs)
    check_canonical_unit_shell_safety(errors, repo_root, registry_entries, canonical_specs)

    if errors:
        print(f"FAILED: {len(errors)} artifact/source authority violation(s).", file=sys.stderr)
        return 1

    print("OK: artifact/source authority is coherent — authored sources, generated mirrors, staging, and outputs are cleanly separated.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
